CREATE TABLE NguoiDung (
	ID INT(11) NOT NULL PRIMARY KEY AUTO_INCREMENT,
	Username VARCHAR(15) NOT NULL,
	Password VARCHAR(15) NOT NULL,
	SoDienThoai VARCHAR(11),
	DiaChi VARCHAR(51),
	Email VARCHAR(20),
	HinhAnh VARCHAR(20),
	Quyen int
);

CREATE TABLE SanPham (
	ID INT(11) NOT NULL PRIMARY KEY AUTO_INCREMENT,
	MaSP VARCHAR(10),
	TenSP VARCHAR(51),
	LoaiSP INT,
	NhaSanXuatID INT,
	XuatXu VARCHAR(50),
	MoTa VARCHAR(51),
	NgayTao DATETIME,
	SoLuong int,
	HinhAnh VARCHAR(20),
	Gia DECIMAL,
	LuotXem int,
	TinhTrang binary
);

CREATE TABLE DanhMuc (
	ID INT(11) NOT NULL PRIMARY KEY AUTO_INCREMENT,
	Ten VARCHAR(51)
);

CREATE TABLE NhaSanXuat (
	ID INT(11) NOT NULL PRIMARY KEY AUTO_INCREMENT,
	Ten VARCHAR(51)
);

CREATE TABLE DiaChiNhanHang (
	ID INT(11) NOT NULL PRIMARY KEY AUTO_INCREMENT,
	NguoiDungID INT,
	TenNguoiNhan VARCHAR(51),
	SoDienThoai VARCHAR(11),
	DiaChiGiaoHang VARCHAR(51),
);

CREATE TABLE DatHang (
	ID INT(11) NOT NULL PRIMARY KEY AUTO_INCREMENT,
	UserID INT,
	TongGia DECIMAL,
	LoaiGiaoHang INT,
	TinhTrang INT,
	NgayTao DATETIME,
	NgayDuKienGiaoHang DATETIME,
	DiaChiNhanHangID INT
);

CREATE TABLE ChiTietDatHang (
	ID INT(11) NOT NULL PRIMARY KEY AUTO_INCREMENT,
	DatHangID INT,
	MaSP INT,
	SoLuong INT,
	Gia DECIMAL,
	TinhTrang INT,
	NgayDuKienGiaoHang DATETIME
);